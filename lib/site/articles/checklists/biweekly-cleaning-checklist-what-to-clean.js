    <ul>
      <li>Change bed linens and straighten blankets, pillows, and visible surfaces.</li>
      <li>Dust nightstands, dressers, headboards, lamps, and window ledges.</li>
      <li>Wipe mirrors or glass surfaces with visible fingerprints.</li>
      <li>Vacuum rugs, the bed perimeter, and the most traveled floor path.</li>
      <li>Check under the bed for dust drift, stray items, or pet hair if accessible.</li>
      <li>Put away clothing piles and clear chairs or benches used as drop zones.</li>
    </ul>
