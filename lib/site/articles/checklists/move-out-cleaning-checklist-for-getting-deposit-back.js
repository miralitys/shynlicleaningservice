  <p>A final preparation tip: do not underestimate odor control. Even if surfaces look clean, pet odor, smoke, old trash smell, and a sour refrigerator can make a unit feel unready. Open windows if possible, remove all garbage, clean drains and disposals if present, and leave the space fully dry before you close it up. Smell is part of what landlords inspect at move out even when it is not written on the checklist.</p>
